// 반복문(for 문) 실습
// 다중 for 문 (반복문의 중첩)을 활용한 구구단 출력 실습

// 실행 예)

/*
[2단]
2 * 1 = 2
2 * 2 =4
  :
2 * 9 = 18

[3단]
  :

9 * 9 = 81
계속하려면 아무 키나 누르세요....
*/
public class Test061
{
	public static void main(String[] args)
	{
		
		int i, j;	// -- 루프 변수
		
		// 바깥쪽 반복문(아우터 루프)   웅~웅~
		for (i=2; i<=9; i++ )                  // 구구단의 단 처리	→ 2                      3                  4                        5                     6                      7                         8 9
		{
			//System.out.println("[i : "  + i+"]");    
			System.out.printf("\n====[%d단]====\n", i);

					 // 안쪽 반복문( '{}'가 교집합처럼 바깥쪽과 교차할 순 없다.
					 // (이너 루프)    쑝쑝쑝~  
					for (j=1; j<=9; j++)         // 곱해지는 수 처리  → 1 2 3 4 5 6 7 8 9   1 2 3 4 5 6 7 8 9   1 2 3 4 5 6 7 8 9         1 2 3 4 5 6 7 8 9    1 2 3 4 5 6 7 8 9        1 2 3 4 5 6 7 8 9                                     
					{
						//System.out.println("j : " + j);
						System.out.printf("%d * %d = %2d%n", i, j, (i*j));
					}

		}				
		
		
	}
}

